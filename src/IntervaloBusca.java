
public class IntervaloBusca {
	private int menor;
	private int maior;
	
	public IntervaloBusca(int menor, int maior) {
		this.menor = menor;
		this.maior = maior;
	}

	public int getMenor() {
		return menor;
	}

	public void setMenor(int menor) {
		this.menor = menor;
	}

	public int getMaior() {
		return maior;
	}

	public void setMaior(int maior) {
		this.maior = maior;
	}
}
